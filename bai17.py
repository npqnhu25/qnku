a = int(input("nhập a số nguyên bất kì"))
b = int(input("nhập b số nguyên bất kì"))
c = int(input("nhập c số nguyên bất kì"))
solon = max(a,b,c)
sonho = min(a,b,c)
print("kết quả số lớn nhất là:", solon)
